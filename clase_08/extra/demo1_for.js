/*
    Declaración: for
    Un ciclo for se repite hasta que una condición especificada se evalúe como false.

    Una declaración for tiene el siguiente aspecto:

    for ([expresión_inicial]; [expresión_condicional]; [expresión_de_actualización]) {
        Bloque de instrucciones
    }
*/

console.log('\n ===== RECORRIDO DE 0 A 9 (for) ===== ');
for (let i = 0; i < 10; i++) {
    console.log(i);
}


console.log('\n ===== RECORRIDO DE 1 A 10 (for) ===== ');
for (let i = 1; i <= 10; i++) {
    console.log(i);
}