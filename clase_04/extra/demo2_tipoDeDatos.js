// Uso de string (cadena de texto)
let pais = "Argentina";                     // El valor se debe encerrar entre comillas dobles o simples
console.log(pais);                          // Imprime el valor de la variable pais en consola


// Uso de number (numérico entero)
let edad = 30;                              // El valor NO se debe encerrar entre comillas
console.log(edad);                          // Imprime el valor de la variable edad en consola


// Uso de number (numérico decimal)
let altura = 1.83;                          // El valor NO se debe encerrar entre comillas
console.log(altura);                        // Imprime el valor de la variable altura en consola


// Uso de boolean (verdadero/falso)
let esMayorDeEdad = true;                   // El valor NO se debe encerrar entre comillas
let esMenorDeEdad = false;                  // El valor NO se debe encerrar entre comillas
console.log(esMayorDeEdad);                 // Imprime el valor de la variable esMayorDeEdad en consola
console.log(esMenorDeEdad);                 // Imprime el valor de la variable esMenorDeEdad en consola


// Uso de undefined (valor indefinido)
let telefono1;                              // Declaración de la variable telefono1 sin definición
console.log(telefono1);                     // Imprime el valor de la variable telefono1 en consola


// Uso de null (valor nulo o desconocido)
let telefono2 = null;                       // Declaración de la variable telefono2 con definición nula
console.log(telefono2);                     // Imprime el valor de la variable telefono2 en consola


// Uso de NaN (valor que no es numérico)
let telefono3 = NaN;                        // Declaración de la variable telefono3 con definición NaN
console.log(telefono3);                     // Imprime el valor de la variable telefono3 en consola