/*
    OPERADORES ARITMETICOS:
        Suma +
        Resta -
        Multiplicación *
        División /
        Módulo %
        Incremento ++
        Decremento --
*/


// Uso de operadores aritméticos
console.log(5 + 6);             // Suma 5+6 e imprime el resultado en consola (11)
console.log(9 - 2);             // Resta 9-2 e imprime el resultado en consola (7)
console.log(4 * 3);             // Multiplica 4*3 e imprime el resultado en consola (12)
console.log(10 / 4);            // Divide 10/4 e imprime el resultado en consola (2.5)
console.log(8 % 2);             // Módulo 8%2 e imprime el resultado en consola (0)

let f = 20;
let g = 25;

f++;                            // Incrementa la variable f
console.log(f);                 // Imprime el valor de la variable f en consola (21)
g--;                            // Decrementa la variable g
console.log(g);                 // Imprime el valor de la variable g en consola (24)